# Напишіть функцію, яка приймає 2 списки. Результатом має бути новий
# список, в якому знаходяться елементи першого списку, яких немає в
# другому. Порядок елементів, що залишилися має відповідати порядку в
# першому (оригінальному) списку. Реалізуйте обчислення за допомогою
# генератора.

def get_difference(list_1, list_2):
    difference_result = (x for x in list_1 if x not in list_2)

    return list(difference_result)


print(get_difference([1, 2, 2, 2, 4, 3, 4], [2]))
